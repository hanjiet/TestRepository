package dataType;

public class TypeCasting {

	public static void main(String[] args) {
		
		/*
		 * Type casting is the process of converting one data type into another data type
		 * 
		 */
		
		double myDouble = 56.98;
		System.out.println("Double value is: "+ myDouble); // double value
		
		//type casting double value into int value
		
		int myInt = (int)myDouble;
		System.out.println("Int value is: "+myInt);// int value 56
		
	}

}
